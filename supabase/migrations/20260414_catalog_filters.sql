-- Normalized filter metadata.
-- Regions intentionally use two levels only: region -> area (for example, 서울 -> 홍대).

create table if not exists regions (
  id         bigint generated by default as identity primary key,
  code       text not null unique,
  name       text not null,
  sort_order integer not null default 0
);

create table if not exists areas (
  id         bigint generated by default as identity primary key,
  region_id  bigint not null references regions(id) on delete cascade,
  code       text not null unique,
  name       text not null,
  sort_order integer not null default 0
);

create table if not exists genres (
  id         bigint generated by default as identity primary key,
  code       text not null unique,
  name       text not null,
  sort_order integer not null default 0
);

create table if not exists theme_genres (
  theme_id bigint not null references themes(id) on delete cascade,
  genre_id bigint not null references genres(id) on delete cascade,
  primary key (theme_id, genre_id)
);

alter table cafes
add column if not exists area_id bigint references areas(id);

