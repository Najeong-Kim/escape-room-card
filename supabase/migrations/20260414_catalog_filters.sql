update cafes
set area_id = areas.id
from areas
where cafes.area_id is null
  and cafes.area_label = areas.name;

alter table regions enable row level security;
alter table areas enable row level security;
alter table genres enable row level security;
alter table theme_genres enable row level security;

drop policy if exists "public read regions" on regions;
create policy "public read regions" on regions
for select using (true);

drop policy if exists "public read areas" on areas;
create policy "public read areas" on areas
for select using (true);

drop policy if exists "public read genres" on genres;
